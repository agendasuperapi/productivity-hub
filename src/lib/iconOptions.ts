// Função para verificar se é um emoji
export const isEmoji = (str: string): boolean => {
  const emojiRegex = /^[\p{Emoji}]$/u;
  return emojiRegex.test(str) || (str.length <= 2 && /[\u{1F300}-\u{1FAD6}]/u.test(str));
};
